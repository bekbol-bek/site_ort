function moveToNextQuestion() {
    currentQuestionIndex++;

    if (currentQuestionIndex < questions.length) {
        showQuestion();
    } else {
        showResults(); // –ó–∞–≤–µ—Ä—à–∞–µ–º —Ç–µ—Å—Ç
    }
}

