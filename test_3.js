    let timeLeft = questionTimeLimit;
    document.getElementById('timer').innerText = `–í—Ä–µ–º—è –Ω–∞ —Ç–µ–∫—É—â–∏–π –≤–æ–ø—Ä–æ—Å:‚è≥: ${timeLeft}—Å–µ–∫—É–Ω–¥`;

